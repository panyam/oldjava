
package com.sri.apps.netsim;
import com.sri.utils.*;
import com.sri.utils.adt.*;

/**
 * Class that holds constants about Protocol numbers and so on.
 */
public class Protocols
{
        /**
         * IP Protocol id.
         */
    public final static short IP_PROTOCOL = 0x0800;

        /**
         * ARP Protocol id.
         */
    public final static short ARP_PROTOCOL = 0x0800;
 
        /**
         * UDP Protocol.
         */
    public final static byte UDP_PROTOCOL = 0x08;
 
        /**
         * TCP Protocol.
         */
    public final static byte TCP_PROTOCOL = 0x06;
 
        /**
         * Ping Protocol.
         */
    public final static byte PING_PROTOCOL = 17;
}
